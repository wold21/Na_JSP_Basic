package tzs;

public class Tzs {
	private int tzsID;
	private String tzsTitle;
	private String userID;
	private String tzsDate;
	private String tzsContent;
	private int tzsAvailable;
	
	
	public int getTzsID() {
		return tzsID;
	}
	public void setTzsID(int tzsID) {
		this.tzsID = tzsID;
	}
	public String getTzsTitle() {
		return tzsTitle;
	}
	public void setTzsTitle(String tzsTitle) {
		this.tzsTitle = tzsTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getTzsDate() {
		return tzsDate;
	}
	public void setTzsDate(String tzsDate) {
		this.tzsDate = tzsDate;
	}
	public String getTzsContent() {
		return tzsContent;
	}
	public void setTzsContent(String tzsContent) {
		this.tzsContent = tzsContent;
	}
	public int getTzsAvailable() {
		return tzsAvailable;
	}
	public void setTzsAvailable(int tzsAvailable) {
		this.tzsAvailable = tzsAvailable;
	}
	
	
	
}
